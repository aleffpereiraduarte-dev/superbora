                <button class="btn btn-next" onclick="next()">Pr√≥ximo</button>
            `;
        }
    }

    function next() {
        if (currentSlide < totalSlides - 1) {
            currentSlide++;
            updateSlide();
            if ('vibrate' in navigator) navigator.vibrate(30);
        }
    }

    function skip() {
        start();
